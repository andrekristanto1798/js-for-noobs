/**
 * Syntax:
 *
 * [If-Else]
 *
 * if (statement1) {
 *   ...
 * } else {
 *   ...
 * }
 *
 * [If-ElseIf-Else]
 *
 * if (statement1) {
 *   ...
 * } else if (statement2) {
 *   ...
 * } else {
 *   ...
 * }
 *
 * [If-ElseIf]
 *
 * if (statement1) {
 *   ...
 * } else if (statement2) {
 *   ...
 * }
 *
 * [For]
 *
 * for (variable; condition; after-loop) {
 *   ...
 * }
 *
 * [While]
 *
 * while (statement) {
 *   ...
 * }
 */

